function openIconWindow() {
  let windowLocation = document.getElementsByClassName("profile-box")[1];

  let window = document.createElement("div");

  let iconsHTML = "";
  icons.forEach((icon) => {
    iconsHTML +=
      "<span onclick='changeAvatar(`" +
      icon +
      "`)' class='" +
      icon +
      " small-avatar'  ></span>";
  });

  window.innerHTML = iconsHTML;

  //   console.log(iconsHTML);
  windowLocation.style.width = "50%";
  windowLocation.style.height = "200px";
  windowLocation.style.display = "flex";
  windowLocation.style.justifyContent = "center";
  windowLocation.style.flexDirection = "row";
  windowLocation.style.overflowY = "scroll";
  windowLocation.style.flexWrap = "wrap";

  windowLocation.innerHTML = window.innerHTML;
}

function changeAvatar(avatar) {
  let userEmail = document.getElementById("userEmail").innerHTML;
  let iconContainer = document.getElementsByClassName("profile-box")[1];
  var xhttp = new XMLHttpRequest();
  xhttp.onreadystatechange = function () {
    if (this.readyState == 4 && this.status == 200) {
      iconContainer.innerHTML =
        "<span class='" + avatar + " big-avatar' ></span>";
      iconContainer.innerHTML +=
        '<i class="fas fa-pencil-alt high-elem" onclick="openIconWindow()" aria-hidden="true"></i>';

      iconContainer.style = "";
    }
  };
  xhttp.open(
    "GET",
    "../private/scripts/changeAvatar.php?userEmail=" +
      userEmail +
      "&avatar=" +
      avatar,
    true
  );
  xhttp.send();
}
